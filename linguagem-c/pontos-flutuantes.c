#include <stdio.h>
// Números de ponto flutuante
int main(){
    // 'long int se utiliza o L'
    long int variavel1 = 5000L;
    // 'Ponto flutuante' utiliza o F
    float variavel2 = 1.0F;
    // 'unsigned' se utiliza o U
    unsigned int variavel3 = 20U;
    printf("Três variáveis: %d%d%d",variavel1,variavel2,variavel3);
    return 0;
}